// Find the end of the nl Set and add to it
// The new whitelist looks like: 'nl': new Set([...words...]),
// We need to find it and add the extra words before the closing ]),
const nlSetStart = content.indexOf("'nl': new Set([");
if (nlSetStart === -1) {
  console.log('ERROR: could not find nl Set');
  process.exit(1);
}

const nlSetEnd = content.indexOf(']),', nlSetStart);
if (nlSetEnd === -1) {
  console.log('ERROR: could not find end of nl Set');
  process.exit(1);
}

const before = content.slice(0, nlSetEnd);
const after = content.slice(nlSetEnd);

const extraStr = extraWords.map(w => "'" + w + "'").join(',');
const newContent = before + ',' + extraStr + after;

fs.writeFileSync(filePath, newContent, 'utf8');
console.log('SUCCESS: added', extraWords.length, 'extra words to nl whitelist');
